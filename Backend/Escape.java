public class Escape {
    public static void main(String[] args) {
        System.out.println("helloAshraful");
        System.out.println("hello\tAshraful");
        System.out.println("hello\nAshraful");
        System.out.println("hello\b Ashraful");
        System.out.println("hello\'Ashraful\'");
        System.out.println("hello \"Ashraful\"");
        System.out.println("hello\\Ashraful\\");
    }
}
